// function
function arg(){
    a=10
    b=20
    c=a+b
    console.log(c)
}
arg()

// with arg
function ar(a,b)
{
    c=a+b
    console.log(c)
}
ar(10,80)

// without arg ,return
function arr(){
    a=10
    b=10
    return a+b
}
console.log(arr())

// with arg, return
function aar(a,b){
return a+b
}
console.log(aar(20,40))


